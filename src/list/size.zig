const Error = @import("./list.zig").Error;
const Node = @import("./list.zig").Node;

pub fn size(comptime T: type, list: ?*Node(T)) Error!usize {
    if (list == null) return Error.InvalidNode;

    var i: usize = 0;
    var current: ?*Node(T) = list;

    while (current) |node| {
        current = node.next;
        i += 1;
    }
    return i;
}
