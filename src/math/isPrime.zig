const std = @import("std");

pub fn isPrime(nbr: u32) bool {
    if (nbr <= 1) return false;
    if (nbr == 2) return true;
    if (nbr % 2 == 0) return false;

    var div: usize = 2;

    while (div < nbr) : (div += 1) {
        if (nbr % div == 0)
            return false;
    }
    return true;
}
