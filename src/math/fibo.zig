const print = @import("std").debug.print;

pub fn fibo(num: u32) u64 {
    if (num == 0) return 0;
    if (num == 1) return 1;

    var n: u32 = num;
    var n1: u32 = 1;
    var n2: u32 = 2;

    while (n > 3) : (n -= 1) {
        const res: u32 = n1 + n2;
        n1 = n2;
        n2 = res;
    }
    return n2;
}
