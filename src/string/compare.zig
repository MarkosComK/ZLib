const length = @import("length.zig").length;

pub fn compare(s1: []const u8, s2: []const u8) bool {
    if (length(s1) != length(s2))
        return false;
    for (s1, s2) |c1, c2| {
        if (c1 != c2)
            return (false);
    }
    return (true);
}
